<h3>February 6, 2016</h3>
<p>
Annual Membership Meeting
<br>
<br>
On January 30, 2016, we held our annual membership meeting at the HOTSA range. New officers and directors were elected and we discussed new business. Quite a few members showed up and we had a good and productive meeting. HOTSA.org is our new website. Information regarding the club, the upcoming matches, the dates of the matches, and general information will be available soon.</p>
<p>Our Benchrest style matches such as the 400 yd. Clay Shoot and the Varmint Silhouette Match will both be held on the 4th Saturday of a month. The first Varmint Silhouette Match will be Saturday, April 23rd, 2016 and the first 400 yd. Clay Shoot will be Saturday, June 25th, 2016. We are not planning matches during July and August, due to the hot weather. Other match dates will be in September and October.  The schedule will be on the website and we will send out emails prior to the matches.</p>
<p>In case you haven’t heard, we are not going to move the range to another site.  We have arranged to continue leasing the property where the range has been for many years.</p>
<p>We had a bulldozer on the range February 1st, working on repairing roads and adding some berms, including a new berm as close to 800 yd. as possible.</p>
<p>At this time, we are planning an Open Range “Practice Day” on Saturday, March 26th. We will set up clay targets and Varmint Silhouette targets from 200 – 500 meters just for fun shooting. Also, shooters are welcome to bring your own targets and shoot at whatever distance you would like. Donations are welcome but there will be <strong>NO CHARGE</strong> for using the range that day. Come to HOTSA and get some practice three weeks before the 400 Yd. Texas Benchrest Clay Match at Utopia, TX. Or just for your fun and shooting practice. Remember to bring your own bench, if you have one. We only have a few benches and they are not very steady.</p>
<p>We look forward to seeing you and appreciate your support of the HOTSA range.</p>
<p>Sincerely,
<br>
Denny Henderson</p>
